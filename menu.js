/* ================= ZOOM CARD ================= */
function zoomCard(src) {
  removeZoom();
  const zoom = document.createElement("div");
  zoom.className = "card-zoom";
  zoom.innerHTML = `<img src="${src}">`;
  zoom.onclick = removeZoom;
  document.getElementById("modal").appendChild(zoom);
}

function removeZoom() {
  const z = document.querySelector(".card-zoom");
  if (z) z.remove();
}

